export function t(chatId, state, key, ...args) {
  const st = state.get(chatId);
  const lang = st?.settings?.language || 'en';
  const text = translations[lang]?.[key] || translations.en[key];
  if (!text) return key;
  return args.reduce((acc, val, i) => acc.replace(`{${i}}`, val), text);
}
